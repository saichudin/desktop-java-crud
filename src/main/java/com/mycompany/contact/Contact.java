    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
       if (TxtName.getText().equals("") || TxtPhone.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Name and Phone is required", "Validation Error", JOptionPane.INFORMATION_MESSAGE);
           return;
       }
       
       if (BtnSave.getText() == "Save") {
           try {
               String sql = "INSERT INTO contacts (name,phone,email,address)"
                       + " VALUES ('"+TxtName.getText()+"','"+TxtPhone.getText()+"','"+TxtEmail.getText()+"','"+TxtAddress.getText()+"')";
               st = cn.createStatement();
               st.executeUpdate(sql);
               ShowData();
               JOptionPane.showMessageDialog(null, "Data Saved");
               ClearTxt();
           } catch (Exception e) {
               JOptionPane.showMessageDialog(null, e);
           }
           
       } else {
           try {
               String id = TblData.getValueAt(TblData.getSelectedRow(), 0).toString();
               String sql = "UPDATE contacts SET name = '"+TxtName.getText()+
                       "', phone = '"+TxtPhone.getText()+"',email = '"+TxtEmail.getText()+
                       "', address = '"+TxtAddress.getText()+"' WHERE id = '"+id+"' ";
               st = cn.createStatement();
               st.executeUpdate(sql);
               ShowData();
               JOptionPane.showMessageDialog(null, "Data Updated");
               ClearTxt();
           } catch (Exception e) {
               JOptionPane.showMessageDialog(null, e);
           }
       }
    }//GEN-LAST:event_BtnSaveActionPerformed

