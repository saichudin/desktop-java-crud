    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        if (TxtName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please select data contact !");
        } else {
            int delete = JOptionPane.showConfirmDialog(null, "Are you sure delete data ?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (delete == 0) {
                try {
                    String id = TblData.getValueAt(TblData.getSelectedRow(), 0).toString();
                    
                    st = cn.createStatement();
                    String sql = "DELETE FROM contacts WHERE id = '"+ id +"'";
                    st.executeUpdate(sql);
                    
                    ShowData();
                    JOptionPane.showMessageDialog(null, "Data Deleted");
                    ClearTxt();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_BtnDeleteActionPerformed

